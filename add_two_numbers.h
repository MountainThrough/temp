#ifndef ADD_TWO_NUMBERS_H
#define ADD_TWO_NUMBERS_H

/**
 * 两数相加函数声明
 * @param a 第一个数
 * @param b 第二个数
 * @return 两数之和
 */
int addTwoNumbers(int a, int b);

/**
 * 两数相加函数（支持浮点数）
 * @param a 第一个浮点数
 * @param b 第二个浮点数
 * @return 两数之和
 */
double addTwoNumbersDouble(double a, double b);

/**
 * 两数相加函数（支持长整数）
 * @param a 第一个长整数
 * @param b 第二个长整数
 * @return 两数之和
 */
long long addTwoNumbersLong(long long a, long long b);

#endif // ADD_TWO_NUMBERS_H
